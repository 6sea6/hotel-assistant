const fs = require('fs');
const path = require('path');

const PROJECT_ROOT = path.resolve(__dirname, '..');
const PRELOAD_BLOCK_START = '// <app-info-generated>';
const PRELOAD_BLOCK_END = '// </app-info-generated>';

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf-8'));
}

function quoteJsString(value) {
  return `'${String(value)
    .replace(/\\/g, '\\\\')
    .replace(/'/g, "\\'")
    .replace(/\r/g, '\\r')
    .replace(/\n/g, '\\n')}'`;
}

function escapeRegExp(value) {
  return String(value).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function buildAppInfo(packageJson) {
  return {
    name: '宾馆比较助手',
    version: packageJson.version,
    releaseDate: '2026-06-12',
    author: packageJson.author || 'Sea'
  };
}

function buildAppInfoObjectSource(appInfo) {
  return `Object.freeze({
  name: ${quoteJsString(appInfo.name)},
  version: ${quoteJsString(appInfo.version)},
  releaseDate: ${quoteJsString(appInfo.releaseDate)},
  author: ${quoteJsString(appInfo.author)}
})`;
}

function buildGeneratedAppInfoSource(appInfo) {
  return `// @ts-check
// This file is generated by scripts/sync-app-info.js. Do not edit by hand.

const APP_INFO = ${buildAppInfoObjectSource(appInfo)};

module.exports = {
  APP_INFO
};
`;
}

function buildPreloadAppInfoBlock(appInfo) {
  return `${PRELOAD_BLOCK_START}
// APP_INFO is synchronized from src/shared/app-info.generated.js by scripts/sync-app-info.js.
// Keep this literal here because sandboxed preload cannot require local project modules.
const APP_INFO = ${buildAppInfoObjectSource(appInfo)};
${PRELOAD_BLOCK_END}`;
}

function replacePreloadAppInfoBlock(preloadSource, appInfo) {
  const nextBlock = buildPreloadAppInfoBlock(appInfo);
  const markerPattern = new RegExp(
    `${escapeRegExp(PRELOAD_BLOCK_START)}[\\s\\S]*?${escapeRegExp(PRELOAD_BLOCK_END)}`
  );

  if (markerPattern.test(preloadSource)) {
    return preloadSource.replace(markerPattern, nextBlock);
  }

  return preloadSource.replace(
    /\/\/ Sandboxed preload cannot[\s\S]*?const APP_INFO = Object\.freeze\(\{[\s\S]*?\}\);/,
    nextBlock
  );
}

function updateReadmeVersion(readmeSource, appInfo) {
  return readmeSource
    .replace(/^# 宾馆比较助手 v.+$/m, `# 宾馆比较助手 v${appInfo.version}`)
    .replace(/宾馆比较助手 v[\d.]+ 主要面向/g, `宾馆比较助手 v${appInfo.version} 主要面向`)
    .replace(/v[\d.]+ 的采集扩充能力包括/g, `v${appInfo.version} 的采集扩充能力包括`);
}

function updateRendererVersionFallbacks(htmlSource, appInfo) {
  return htmlSource
    .replace(/id="aboutVersionText">v[\d.]+</g, `id="aboutVersionText">v${appInfo.version}<`)
    .replace(/id="manualVersionText">v[\d.]+</g, `id="manualVersionText">v${appInfo.version}<`)
    .replace(
      /id="aboutUpdateDateText">[\d-]+</g,
      `id="aboutUpdateDateText">${appInfo.releaseDate}<`
    );
}

function writeIfChanged(filePath, content) {
  const previous = fs.existsSync(filePath) ? fs.readFileSync(filePath, 'utf-8') : '';
  if (previous === content) {
    return false;
  }

  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, content, 'utf-8');
  return true;
}

function syncAppInfoFiles(projectRoot = PROJECT_ROOT) {
  const packageJson = readJson(path.join(projectRoot, 'package.json'));
  const appInfo = buildAppInfo(packageJson);
  const generatedPath = path.join(projectRoot, 'src', 'shared', 'app-info.generated.js');
  const preloadPath = path.join(projectRoot, 'src', 'main', 'preload.js');
  const readmePath = path.join(projectRoot, 'README.md');
  const rendererIndexPath = path.join(projectRoot, 'src', 'renderer', 'index.html');
  const rendererManualPath = path.join(projectRoot, 'src', 'renderer', 'manual.html');
  const preloadSource = fs.readFileSync(preloadPath, 'utf-8');
  const readmeSource = fs.readFileSync(readmePath, 'utf-8');
  const rendererIndexSource = fs.readFileSync(rendererIndexPath, 'utf-8');
  const rendererManualSource = fs.readFileSync(rendererManualPath, 'utf-8');

  return {
    appInfo,
    changed: {
      generated: writeIfChanged(generatedPath, buildGeneratedAppInfoSource(appInfo)),
      preload: writeIfChanged(preloadPath, replacePreloadAppInfoBlock(preloadSource, appInfo)),
      readme: writeIfChanged(readmePath, updateReadmeVersion(readmeSource, appInfo)),
      rendererIndex: writeIfChanged(
        rendererIndexPath,
        updateRendererVersionFallbacks(rendererIndexSource, appInfo)
      ),
      rendererManual: writeIfChanged(
        rendererManualPath,
        updateRendererVersionFallbacks(rendererManualSource, appInfo)
      )
    }
  };
}

if (require.main === module) {
  const { appInfo, changed } = syncAppInfoFiles(PROJECT_ROOT);
  const changedFiles = Object.entries(changed)
    .filter(([, didChange]) => didChange)
    .map(([name]) => name)
    .join(', ');
  console.log(`App info synced for v${appInfo.version}${changedFiles ? ` (${changedFiles})` : ''}`);
}

module.exports = {
  buildAppInfo,
  buildGeneratedAppInfoSource,
  replacePreloadAppInfoBlock,
  syncAppInfoFiles,
  updateRendererVersionFallbacks,
  updateReadmeVersion
};
